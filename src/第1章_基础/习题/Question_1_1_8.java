public class Question_1_1_8 {
    public static void main(String[] args) {
        // 输出字符 'b'
        System.out.println('b');
        // 字符 'b' 与 'c' 相加，结果为整数，为 b c ASCII 码值之和
        System.out.println('b' + 'c');
        // 'a' + 4 结果转换为字符为 a 之后第四个字符：e
        System.out.println((char)('a' + 4));
    }
}
